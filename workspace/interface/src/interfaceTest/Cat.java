package interfaceTest;

public class Cat implements Pet{

	@Override
	public void giveYourHand() {
		System.out.println("그냥간다.");
	}

	@Override
	public void bang() {
		System.out.println("못들은척 한다.");
	}

	@Override
	public void sitDown() {
		System.out.println("무시한다.");
	}

	@Override
	public void waitNow() {
		System.out.println("안기다린다.");
	}

	@Override
	public void getNose() {
		System.out.println("하품을 한다.");
	}
	

}
